public class Main {
	public static void main(String[] args) {
		System.out.println(Math.max(1, 2));
		System.out.println(Math.max(2, 1));
		System.out.println(Math.max(1.0, 2.0));
		System.out.println(Math.max(2.0, 1.0));
		System.out.println(Math.max(1, 2.0));
		System.out.println(Math.max(2, 1.0));

		System.out.println(Math.min(1, 2));
		System.out.println(Math.min(2, 1));
		System.out.println(Math.min(1.0, 2.0));
		System.out.println(Math.min(2.0, 1.0));
		System.out.println(Math.min(1, 2.0));
		System.out.println(Math.min(2, 1.0));
	}
}
